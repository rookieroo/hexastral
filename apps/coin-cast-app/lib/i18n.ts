const DICT: Record<string, Record<keyof typeof EN, string>> = {
  en: EN,
  zh: ZH,
  'zh-Hant': ZH_HANT,
  ja: JA,
  ko: KO,
}

function normalizeLocale(input: string): UiLocale {
  if (input in DICT) return input as UiLocale
  if (input === 'zh-CN' || input.startsWith('zh-CN')) return 'zh'
  if (input.startsWith('zh-Hant') || input === 'zh-TW' || input === 'zh-HK') return 'zh-Hant'
  if (input.startsWith('zh')) return 'zh'
  const base = input.split('-')[0] ?? 'en'
  if (base in DICT) return base as UiLocale
  return 'en'
}

export type SatelliteLocaleKey = keyof typeof EN

export function useSatelliteI18n() {
  const [locale, setLocale] = useState<UiLocale>('en')

  useEffect(() => {
    ;(async () => {
      const saved = await AsyncStorage.getItem(LOCALE_STORAGE_KEY)
      setLocale(resolveUiLocale(saved))
    })()
  }, [])

  const t = (key: SatelliteLocaleKey, vars?: Record<string, string | number>): string => {
    const row = DICT[locale] ?? DICT.en
    const base = (row?.[key] ?? EN[key] ?? key) as string
    if (!vars) return base
    return Object.entries(vars).reduce((acc, [k, v]) => acc.replaceAll(`{${k}}`, String(v)), base)
  }

  /** API / backend locale tag (svc-astro i18n-prompt supports en/zh/zh-Hant/ja/ko) */
  const apiLocale =
    locale === 'zh'
      ? 'zh'
      : locale === 'zh-Hant'
        ? 'zh-Hant'
        : locale === 'ja'
          ? 'ja'
          : locale === 'ko'
            ? 'ko'
            : 'en'

  return { locale: apiLocale, uiLocale: locale, t }
}
