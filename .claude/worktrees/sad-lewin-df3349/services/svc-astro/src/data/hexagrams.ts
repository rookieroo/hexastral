/**
 * 通过卦序号获取卦象
 */
export function getHexagramByNumber(num: number): Hexagram | undefined {
  return HEXAGRAMS.find((h) => h.number === num)
}

/**
 * 通过上下卦名获取卦象
 * @param upper 上卦 trigram key (e.g. 'qian', 'kun')
 * @param lower 下卦 trigram key
 */
export function getHexagramByTrigrams(upper: string, lower: string): Hexagram | undefined {
  return HEXAGRAMS.find((h) => h.upperTrigram === upper && h.lowerTrigram === lower)
}

/** 八卦索引映射：三位二进制 → trigram key */
const TRIGRAM_MAP: Record<string, string> = {
  '111': 'qian', // ☰ 乾
  '110': 'dui', // ☱ 兑
  '101': 'li', // ☲ 离
  '100': 'zhen', // ☳ 震
  '011': 'xun', // ☴ 巽
  '010': 'kan', // ☵ 坎
  '001': 'gen', // ☶ 艮
  '000': 'kun', // ☷ 坤
}

/**
 * 通过六爻（6 位二进制）生成卦象
 * @param lines 6 个 0/1 数组（从下到上：初爻 → 上爻）
 *   1 = 阳爻（—）, 0 = 阴爻（- -）
 */
export function getHexagramByLines(lines: number[]): Hexagram | undefined {
  if (lines.length !== 6) return undefined
  const lower = `${lines[0]}${lines[1]}${lines[2]}`
  const upper = `${lines[3]}${lines[4]}${lines[5]}`
  const lowerKey = TRIGRAM_MAP[lower]
  const upperKey = TRIGRAM_MAP[upper]
  if (!lowerKey || !upperKey) return undefined
  return getHexagramByTrigrams(upperKey, lowerKey)
}
