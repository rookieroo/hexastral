function normalizeLocale(input: string): string {
  const lower = input.toLowerCase()
  if (lower.includes('hant') || input === 'zh-TW' || input === 'zh-HK') return 'zh-Hant'
  if (DICT[input]) return input
  const base = input.split('-')[0] ?? 'en'
  return DICT[base] ? base : 'en'
}

export function useSatelliteI18n() {
  const [locale, setLocale] = useState('en')

  useEffect(() => {
    ;(async () => {
      const saved = await AsyncStorage.getItem(LOCALE_STORAGE_KEY)
      const fallback = getLocales()[0]?.languageTag ?? 'en'
      setLocale(normalizeLocale(saved ?? fallback))
    })()
  }, [])

  const fallback = DICT.en ?? {}
  const t = (key: keyof (typeof DICT)['en'], vars?: Record<string, string | number>): string => {
    const row = DICT[locale] ?? fallback
    const base = (row[key] ?? fallback[key] ?? String(key)) as string
    if (!vars) return base
    return Object.entries(vars).reduce((acc, [k, v]) => acc.replaceAll(`{${k}}`, String(v)), base)
  }

  return { locale, t }
}
