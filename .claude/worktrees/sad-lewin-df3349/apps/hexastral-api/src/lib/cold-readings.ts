/** 获取冷读主句（携带随机性防重复） */
export function getColdReading(dayMaster: string, monthBranch?: string): string {
  const readings = DAY_MASTER_READINGS[dayMaster]
  if (!readings || readings.length === 0) {
    return '星辰正在解析你的命盘，一生的答案藏在四柱之中。'
  }

  // 随机取一条主线
  const main =
    readings[Math.floor(Math.random() * readings.length)] ??
    readings[0] ??
    '星辰为你写就了独一无二的命运格局，答案藏在四柱之中。'

  // 附加月支微调（50% 概率）
  if (monthBranch && MONTH_BRANCH_MODIFIERS[monthBranch] && Math.random() > 0.5) {
    return `${main}\n\n${MONTH_BRANCH_MODIFIERS[monthBranch]}`
  }

  return main
}

